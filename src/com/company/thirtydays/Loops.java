package com.company.thirtydays;

import java.util.Scanner;

public class Loops {

    public static void main(String[] args) {
        int n = 0;

        for (int i = 1; i <= 10; i++) {
            int result = n * i;
            System.out.println(n + " x " + i + " = " + result);
        }
    }
}
